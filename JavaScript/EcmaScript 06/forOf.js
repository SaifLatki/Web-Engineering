// for of loop is used to iterate over the elements of an array or string. 
//It is a new way to loop over the elements of an array. It is introduced in ECMAScript 6.

//using for of loop to iterate over the elements of an array
const cars = ['BMW', 'Audi', 'Mercedes', 'Toyota', 'Ford'];

for (const car of cars) {
    console.log(car);
}

//using for of loop to iterate over the characters of a string
const str = 'hello';

for (const char of str) {
    console.log(char);
}


